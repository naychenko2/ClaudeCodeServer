## ВОССТАНОВЛЕНИЕ ПОСЛЕ СБОЕВ

- **Документация не находится** — клонируй репозиторий, читай исходники и README напрямую
- **Поиск кода без результатов** — расширь запрос, ищи по концепции вместо точного имени
- **Rate limit API gh** — используй клонированный репозиторий во временном каталоге
- **Репозиторий не найден** — поищи форки или зеркала
- **Sitemap не найден** — попробуй `/sitemap-0.xml`, `/sitemap_index.xml` или забери индексную страницу доков и разбери навигацию
- **Версионированные доки не найдены** — откатись на последнюю версию и отметь это в ответе
- **Не уверен** — **ЗАЯВИ О СВОЕЙ НЕУВЕРЕННОСТИ**, предложи гипотезу

