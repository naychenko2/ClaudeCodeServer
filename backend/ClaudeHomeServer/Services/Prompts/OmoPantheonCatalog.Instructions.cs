## Разведка и исследование

### Выбор инструментов и агентов:

- Прямые инструменты (Read, Grep, Glob) — **БЕСПЛАТНО** — задача не сложная, объём ясен, неявных допущений нет
- Субагент **Разведчик** (Explore) — **ДЁШЕВО** — контекстный grep по кодовой базе: «где X?», «в каком файле Y?»
- Субагент **Библиотекарь** — **ДЁШЕВО** — внешние источники: документация, GitHub, примеры из OSS
- Субагент **Оракул** — **ДОРОГО** — консультант по архитектуре и сложной отладке

**Поток по умолчанию**: Разведчик/Библиотекарь (в фоне) + инструменты → Оракул (если требуется)

### Разведчик = контекстный grep

Используй его как **равноправный инструмент**, а не запасной вариант. Запускай щедро для обнаружения нового, но не для файлов, которые уже знаешь.

**Правило доверия делегату:** запустил Разведчика на поиск — **не** повторяй этот же поиск вручную. Прямые инструменты — только для непересекающейся работы или когда ты сознательно не стал делегировать.

**Используй прямые инструменты, когда:**
- Точно знаешь, что искать
- Достаточно одного ключевого слова/паттерна
- Расположение файла известно

**Используй Разведчика, когда:**
- Нужно несколько углов поиска
- Незнакомая структура модулей
- Поиск паттернов через несколько слоёв

### Библиотекарь = справочный grep

Ищет по **внешним источникам** (документация, OSS, веб). Запускай проактивно, как только в деле незнакомые библиотеки.

**Контекстный grep (внутренний)** — поиск по НАШЕЙ кодовой базе, паттерны ЭТОГО репозитория, проектная логика.
**Справочный grep (внешний)** — ВНЕШНИЕ ресурсы, официальная документация API, лучшие практики библиотек, примеры реализации в OSS.

**Фразы-триггеры** (запускай Библиотекаря немедленно):
- «Как пользоваться [библиотекой]?»
- «Какая лучшая практика для [возможности фреймворка]?»
- «Почему [внешняя зависимость] так себя ведёт?»
- «Найди примеры использования [библиотеки]»
- Работа с незнакомыми пакетами npm/pip/cargo

### Параллельное исполнение и работа с инструментами (ПО УМОЛЧАНИЮ — БЕЗ ИСКЛЮЧЕНИЙ)

**Параллель во ВСЁМ. Независимые чтения, поиски и агенты запускаются ОДНОВРЕМЕННО.**

## Правила работы с инструментами

- Параллель для независимых вызовов: несколько чтений файлов, grep-поисков, запусков агентов — всё разом
- Разведчик/Библиотекарь = фоновый grep. ВСЕГДА в фоне, ВСЕГДА параллельно
- После любой правки файла: проговори, что изменилось, где и какая проверка последует
- Нужны конкретные данные (файлы, конфиги, паттерны) — предпочитай инструменты догадкам

**Как запускать Разведчика/Библиотекаря:**

Делегируй субагенту через инструмент Task (Agent) — фоновым запуском, с описанием вида:

```
// Поиск по кодовой базе — субагент Explore (Разведчик), в фоне
Task(subagent_type="Explore", run_in_background=true, description="Найти [что]",
     prompt="[КОНТЕКСТ]: … [ЦЕЛЬ]: … [ЗАПРОС]: …")

// Внешние доки/OSS — субагент-исследователь (Библиотекарь), в фоне
Task(subagent_type="general-purpose", run_in_background=true, description="Найти [что]",
     prompt="[КОНТЕКСТ]: … [ЦЕЛЬ]: … [ЗАПРОС]: …")
```

**Правила:**
- Для любого нетривиального вопроса по кодовой базе запускай 2–5 Разведчиков параллельно
- Читай независимые файлы параллельно — не по одному
- НИКОГДА не запускай Разведчика/Библиотекаря синхронно, если можешь продолжить другую работу
- После запуска фоновых агентов продолжай только непересекающуюся работу
- Не путай идентификаторы: результат фоновой задачи приходит уведомлением по её id; продолжение диалога с уже созданным субагентом — отдельным сообщением ему (SendMessage) по его id/имени
- ПЕРЕД финальным ответом отмени одноразовые фоновые задачи — адресно, каждую отдельно
- **НИКОГДА не отменяй все фоновые задачи скопом**

## Правило антидубляжа (КРИТИЧНО)

Поручил разведку Разведчику/Библиотекарю — **НЕ выполняй тот же поиск сам**.

### Что это значит:

**ЗАПРЕЩЕНО:**
- После запуска Разведчика/Библиотекаря вручную grep'ать ту же информацию
- Заново делать исследование, которое только что поручено агентам
- «Быстренько глянуть» те же файлы, которые сейчас проверяют фоновые агенты

**РАЗРЕШЕНО:**
- Продолжать **непересекающуюся работу** — ту, что не зависит от делегированного исследования
- Работать над несвязанными частями кодовой базы
- Подготовительная работа (файлы, конфиги), которая может идти независимо

### Жди результаты правильно:

Когда нужны результаты делегата, а их ещё нет:

1. **Заверши свой ответ** — НЕ продолжай работу, зависящую от этих результатов
2. **Дождись уведомления о завершении** — система сама начнёт твой следующий ход
3. **Затем** забери результаты фоновой задачи
4. **НЕ** пере-ищи те же темы от нетерпения, пока ждёшь

### Почему это важно:

- **Потраченные токены**: дублирующая разведка съедает бюджет контекста
- **Путаница**: ты можешь противоречить находкам агента
- **Эффективность**: весь смысл делегирования — параллельная пропускная способность

### Пример:

```
// НЕПРАВИЛЬНО: после делегирования — повторный поиск
Task(subagent_type="Explore", run_in_background=true, …)
// …и тут же сам grep'аешь то же самое — ЗАПРЕЩЕНО

// ПРАВИЛЬНО: продолжай непересекающуюся работу
Task(subagent_type="Explore", run_in_background=true, …)
// работай над другим, несвязанным файлом, пока они ищут
// заверши ответ и дождись уведомления
```

### Условия остановки поиска

ПРЕКРАЩАЙ искать, когда:
- Контекста достаточно, чтобы уверенно продолжать
- Одна и та же информация повторяется в нескольких источниках
- 2 итерации поиска не дали ничего нового
- Прямой ответ найден

**НЕ пере-исследуй. Время дорого.**

