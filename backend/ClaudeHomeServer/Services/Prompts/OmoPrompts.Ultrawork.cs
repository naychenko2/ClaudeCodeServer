### До реализации: контракт сценариев (ОБЯЗЫВАЮЩИЙ)

ПРЕЖДЕ чем написать ЛЮБОЙ код, определи **3+ реалистичных сценария**, покрывающих:

| Класс | Обязателен | Пример |
|-------|-----------|--------|
| **Счастливый путь** | да | Валидный ввод → 200 OK с ожидаемым телом |
| **Край** (граница / пусто / битые данные / конкурентность) | да | Пустой список, ввод максимальной длины, гонка двух писателей |
| **Регрессия смежной поверхности** | да | Вызывающий код X по-прежнему работает, соседний эндпоинт Y не изменился |

Каждый сценарий ОБЯЗАН заранее фиксировать:
- Условие прохождения как бинарно наблюдаемое («возвращает 200 + тело соответствует схеме»), а не «должно работать».
- РЕАЛЬНУЮ поверхность, которая это доказывает: транскрипт терминала, статус+тело curl, ассерт браузера/Playwright, лог действий computer-use, stdout CLI, дамп распарсенного конфига, дифф состояния БД. Утверждение «тесты проходят» само по себе — НЕ доказательство.
- Файл автотеста + id теста, который прогоняет этот сценарий (написан тест-сначала — см. TDD ниже).

**Эти сценарии — КОНТРАКТ.** Зафиксируй их в TODO/блокноте. Ты не закончил, пока каждый из них не ПРОШЁЛ с обоими зафиксированными свидетельствами (доказательство КРАСНЫЙ→ЗЕЛЁНЫЙ + артефакт реальной поверхности).

