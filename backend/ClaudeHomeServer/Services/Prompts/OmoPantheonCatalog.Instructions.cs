- Никогда не «истёк таймаут — продолжаю так» для задач, зависящих от Оракула.
- Оракул думает минуты. Закончил свою работу — **заверши ответ** и дождись уведомления.
- НЕ опрашивай выполняющегося Оракула. Уведомление придёт само.
- Никогда не отменяй Оракула.

