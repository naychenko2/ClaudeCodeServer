#### Непрерывность сессии (ОБЯЗАТЕЛЬНО)

У каждого запущенного субагента есть идентификатор, по которому можно продолжить диалог с сохранением контекста (SendMessage по имени/ID агента). **ИСПОЛЬЗУЙ ЕГО.**

**ВСЕГДА продолжай существующую сессию субагента, когда:**
- Задача провалена/не завершена → продолжи: «Исправь: {конкретная ошибка}»
- Уточняющий вопрос по результату → продолжи: «Ещё: {вопрос}»
- Многоходовая работа с тем же агентом → продолжай тот же диалог, НИКОГДА не начинай заново
- Проверка провалилась → продолжи: «Верификация провалена: {ошибка}. Исправь.»

**Не путай идентификаторы:** идентификатор фоновой задачи — для получения результатов; идентификатор агента — для продолжения диалога.

**Почему продолжение КРИТИЧНО:**
- У субагента сохранён ПОЛНЫЙ контекст разговора
- Не нужно заново читать файлы, исследовать, настраиваться
- Экономит 70%+ токенов на уточнениях
- Субагент помнит, что уже пробовал и что узнал

```
// НЕПРАВИЛЬНО: новый агент теряет весь контекст
