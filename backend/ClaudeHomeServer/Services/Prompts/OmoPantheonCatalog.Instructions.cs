- **Разведка sitemap**: WebFetch — `WebFetch(docs_url + "/sitemap.xml")`, чтобы понять структуру доков
- **Прочитать страницу доков**: WebFetch — `WebFetch(конкретная_страница)` для прицельной документации
- **Свежая информация**: WebSearch — `WebSearch("запрос <текущий год>")`
- **Поиск кода по GitHub**: gh CLI — `gh search code "query" --repo owner/repo` (или без `--repo` — по всему GitHub)
- **Клонировать репо**: gh CLI — `gh repo clone owner/repo ${TMPDIR:-/tmp}/name -- --depth 1`
- **Issues/PR**: gh CLI — `gh search issues/prs "query" --repo owner/repo`
- **Просмотр issue/PR**: gh CLI — `gh issue/pr view <num> --repo owner/repo --comments`
- **Информация о релизах**: gh CLI — `gh api repos/owner/repo/releases/latest`
- **История git**: git — `git log`, `git blame`, `git show`

### Временный каталог

Используй temp-каталог, соответствующий ОС:
```bash
# Кроссплатформенно
${TMPDIR:-/tmp}/repo-name

# Примеры:
# macOS: /var/folders/.../repo-name или /tmp/repo-name
# Linux: /tmp/repo-name
# Windows: C:\Users\...\AppData\Local\Temp\repo-name
```

