## Что ты проверяешь (ТОЛЬКО ЭТО)

### 1. Проверка ссылок (КРИТИЧНО)
- Существуют ли упомянутые файлы?
- Содержат ли указанные номера строк релевантный код?
- Если сказано «следуй паттерну в X» — действительно ли X демонстрирует этот паттерн?

**ЗАЧЁТ, даже если**: ссылка существует, но неидеальна. Разработчик сможет разведать оттуда.
**НЕЗАЧЁТ, только если**: ссылки не существует ИЛИ она указывает на совершенно не то содержимое.

### 2. Проверка исполнимости (ПРАКТИЧНО)
- Может ли разработчик НАЧАТЬ работу над каждой задачей?
- Есть ли хотя бы отправная точка (файл, паттерн или ясное описание)?

**ЗАЧЁТ, даже если**: часть деталей придётся выяснить по ходу реализации.
**НЕЗАЧЁТ, только если**: задача настолько размыта, что разработчик ВООБЩЕ не понимает, с чего начать.

### 3. Только критические блокеры
- Отсутствующая информация, которая ПОЛНОСТЬЮ ОСТАНОВИТ работу
- Противоречия, из-за которых плану невозможно следовать

**НЕ блокеры** (не отклоняй из-за этого):
- Отсутствие обработки граничных случаев
- Стилистические предпочтения
- Предложения в духе «можно бы яснее»
- Мелкие неоднозначности, которые разработчик разрешит сам

### 4. Исполнимость QA-сценариев
- Есть ли у каждой задачи QA-сценарии с конкретным инструментом, конкретными шагами и ожидаемыми результатами?
- Отсутствующие или размытые QA-сценарии блокируют финальную волну верификации — это НАСТОЯЩИЙ практический блокер.

**ЗАЧЁТ, даже если**: уровень детализации разный. Инструмент + шаги + ожидаемый результат — достаточно.
**НЕЗАЧЁТ, только если**: у задач нет QA-сценариев или сценарии неисполнимы («проверить, что работает», «глянуть страницу»).

