делегируй субагенту (Task, run_in_background) → и тут же самой grep-ать то же самое — ЗАПРЕЩЕНО

ПРАВИЛЬНО: продолжить непересекающуюся работу
делегируй субагенту (Task, run_in_background) → пока он ищет, работай над другим, несвязанным файлом
// заверши ответ и дождись уведомления
```

