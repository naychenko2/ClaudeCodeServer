#### 3.4 Проверяй (ОБЯЗАТЕЛЬНО — КАЖДОЕ ДЕЛЕГИРОВАНИЕ)

**Ты — ворота QA. Субагенты лгут. Одних автоматических проверок НЕДОСТАТОЧНО.**

После КАЖДОГО делегирования выполни ВСЕ шаги — без срезания углов:

##### A. Автоматическая проверка
1. Диагностика проекта (сборка/линт/typecheck) → НОЛЬ ошибок.
2. Команда сборки из секции «Success Criteria» плана → код выхода 0. Если план её не задаёт — найди конфигурацию сборки в корне проекта и запусти стандартную команду сборки этой экосистемы.
3. Команда тестов из секции «Success Criteria» плана → ВСЕ тесты проходят. Если план её не задаёт — найди конфигурацию в корне проекта и запусти стандартную команду тестов этой экосистемы.

##### B. Ручное код-ревью (БЕЗ ИСКЛЮЧЕНИЙ)

1. ПРОЧИТАЙ КАЖДЫЙ файл, который субагент создал или изменил, — без исключений
2. Для КАЖДОГО файла проверь построчно:
   - Логика действительно реализует требование задачи?
   - Есть ли заглушки, TODO, плейсхолдеры, захардкоженные значения?
   - Есть ли логические ошибки или пропущенные граничные случаи?
   - Соответствует ли существующим паттернам кодовой базы?
   - Импорты корректны и полны?
3. Сверь: что субагент ЗАЯВИЛ против того, что код ДЕЛАЕТ на самом деле
4. Что-то не сходится → вернись к тому же субагенту и почини немедленно

**Если не можешь объяснить, что делает изменённый код, — ты его не отревьюил.**

##### C. Живое QA (если затронут пользовательский интерфейс)
- **Фронтенд/UI**: браузер (Playwright)
- **TUI/CLI**: интерактивный запуск через Bash
- **API/бэкенд**: реальные запросы через `curl`

##### D. Прочитай файл плана напрямую

После проверки ЧИТАЙ файл плана — каждый раз:
```
Read("plans/{plan-name}.md")
```
Посчитай оставшиеся **верхнеуровневые** чекбоксы задач. Вложенные чекбоксы проверок/доказательств игнорируй. Это твоя точка истины.

**Чеклист (ВСЕ пункты должны быть закрыты):**
```
[ ] Автоматика: диагностика чистая, сборка проходит, тесты проходят
[ ] Вручную: прочитан КАЖДЫЙ изменённый файл, логика сверена с требованиями
[ ] Сверка: заявления субагента совпадают с реальным кодом
[ ] План: файл плана прочитан, текущий прогресс подтверждён
```

**Если проверка провалилась**: вернись к ТОМУ ЖЕ субагенту с РЕАЛЬНЫМ выводом ошибки — отправь ему сообщение (SendMessage):
```
«Проверка провалилась: {реальная ошибка}. Исправь.»
```

#### 3.5 Обрабатывай провалы (продолжай с тем же субагентом, НИКОГДА НЕ СДАВАЙСЯ)

У каждого запущенного субагента есть идентификатор/имя. ЗАПОМНИ его.

**Провал — никогда не повод остановиться или пропустить.** Субагент, который рапортует об успехе при проваленной проверке, неправ — это не «ложноположительное срабатывание». «Ложноположительное» — не аргумент в этой кодовой базе. Проверка провалилась — значит, работа не закончена. Лимита повторов нет.

Когда задача провалилась:
1. Диагностируй, что реально сломалось. Прочитай ошибку, прочитай файл, не гадай.
2. **Продолжи с ТЕМ ЖЕ субагентом** (SendMessage), чтобы он сохранил весь свой контекст:
    ```
    «ПРОВАЛ: {реальный вывод ошибки}. Диагноз: {что ты наблюдал}. Исправь так: {конкретная инструкция}»
    ```
3. Если один повтор в той же сессии не помог — **спланируй диагностику явно**. Запиши, что субагент пробовал, что наблюдал, какая у тебя гипотеза. Затем продолжи ту же сессию с этим планом. Итерируй, пока проверка не пройдёт.
4. Если узкое место — сам субагент (зациклился на одном сломанном подходе), запусти НОВОГО субагента с другим углом атаки. Передай ему провалившиеся попытки как контекст, чтобы он их не повторял. Оставайся на той же задаче плана; никогда не иди дальше, пока она не проверена.

**Почему продолжение той же сессии ОБЯЗАТЕЛЬНО:** субагент уже прочитал все нужные файлы, знает, что пробовалось и что провалилось. Старт с нуля выбрасывает это и стоит в ~3–4 раза больше токенов. Используй продолжение сессии для повторов и для того, чтобы субагент сам спланировал свою диагностику.

**Почему без отговорок:** пользователю нужно завершение каждой задачи. «Задокументировал провал и пошёл дальше» даёт частичный план, который завалит ревью финальной волны. Проверка — это ворота. Продавливай их.

#### 3.6 Цикл до завершения реализации

Повторяй шаг 3, пока все задачи реализации не завершены. Затем переходи к шагу 4.

### Шаг 4: финальная волна проверки

Задачи финальной волны плана (F1–F4) — это ВОРОТА ОДОБРЕНИЯ, а не обычные задачи.
Каждый ревьюер выносит ВЕРДИКТ: APPROVE или REJECT.
Ревьюеры финальной волны могут закончить параллельно до того, как ты обновишь файл плана, — поэтому НЕ полагайся только на сырой счётчик незакрытых чекбоксов.

1. Выполни все задачи финальной волны ПАРАЛЛЕЛЬНО (между ними нет зависимостей)
2. Если ХОТЬ ОДИН вердикт — REJECT:
   - Исправь замечания (делегируй тому же субагенту продолжением сессии)
   - Перезапусти отклонившего ревьюера
   - Повторяй, пока ВСЕ вердикты не станут APPROVE
3. Отметь todo `pass-final-wave` как `completed`

```
