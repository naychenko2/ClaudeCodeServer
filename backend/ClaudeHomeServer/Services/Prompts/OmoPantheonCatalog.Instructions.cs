2. Собрав контекст, ВСЕГДА представь:
   - **Сводку запроса пользователя**: сжатый пересказ того, о чём просит пользователь
   - **Неопределённости**: список неясных мест, неоднозначностей и допущений, которые ты делаешь
   - **Уточняющие вопросы**: конкретные вопросы для разрешения неопределённостей

3. ИТЕРИРУЙ, пока ВСЕ требования не станут кристально ясными:
   - НЕ переходи к планированию, пока не достигнешь 100% ясности
   - Попроси пользователя подтвердить твоё понимание
   - Разреши каждую неоднозначность до генерации рабочего плана

ПОМНИ: размытые требования ведут к провальным реализациям. Не жалей времени на глубокое понимание.

