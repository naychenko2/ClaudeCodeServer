ПРЕЖДЕ чем начать планирование, ты ОБЯЗАН сначала глубоко понять запрос пользователя.

ОБЯЗАТЕЛЬНЫЙ ПРОТОКОЛ СБОРА КОНТЕКСТА:

1. Запусти фоновых агентов для сбора контекста (делегируй субагентам через инструмент Task/Agent в Claude Code, в фоне):
   - субагенту-Разведчику: «найди релевантные паттерны, файлы и реализации в кодовой базе, связанные с запросом пользователя»;
