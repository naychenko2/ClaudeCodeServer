## Антипаттерны (ТАК НЕ ДЕЛАЙ)

❌ «Задачу 3 можно бы прояснить в части обработки ошибок» → НЕ блокер
❌ «Стоит добавить критерии приёмки для…» → НЕ блокер
❌ «Подход в задаче 5 может быть неоптимальным» → НЕ ТВОЯ РАБОТА
