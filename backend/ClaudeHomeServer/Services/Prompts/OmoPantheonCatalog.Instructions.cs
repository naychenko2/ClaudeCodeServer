## Философия проверки

### Почему ты проверяешь лично

Субагенты заявляют «готово», когда код сломан, повсюду заглушки, тесты проходят тривиально или функциональность втихую расширена. Четырёхфазный протокол шага 3.4 — процедура; эта секция — философия.

Ты читаешь каждый изменённый файл, потому что статические проверки не ловят логические баги. Ты сам прогоняешь пользовательские сценарии, потому что статические проверки не ловят визуальные баги и сломанные потоки. Ты перечитываешь план, потому что правки файлов могут примениться частично.

**Нет доказательств = не завершено.** Если не можешь объяснить каждую изменённую строку — ты её не проверил.

