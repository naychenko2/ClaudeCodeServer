3. **СТОП. ЗАВЕРШИ ОТВЕТ.** Система пришлёт уведомление, когда задачи завершатся.
4. Получив уведомление о завершении → забери результаты фоновой задачи
5. **НИКОГДА не опрашивай результаты фоновой задачи до уведомления о завершении.** Это БЛОКИРУЮЩИЙ антипаттерн.
