using System.Text.RegularExpressions;

namespace ClaudeHomeServer.Services.Prompts;

// Рантайм-тексты, перенесённые из oh-my-openagent по договорённости с авторами
// (русский перевод — обязательное условие; соответствие оригиналам — docs/omo-adoption.md,
// полные переводы — docs/omo/translations/).
public static partial class OmoPrompts
{
    // --- Магическое слово ultrawork (флаг ultrawork-keyword) ---

    // ultrawork/ulw — как в оригинале; «ультра» — русский эквивалент отдельным словом
    [GeneratedRegex(@"(?<![\p{L}\p{N}])(ultrawork|ulw|ультраворк|ультра)(?![\p{L}\p{N}])",
        RegexOptions.IgnoreCase | RegexOptions.CultureInvariant)]
    private static partial Regex UltraworkKeyword();

    public static bool ContainsUltraworkKeyword(string text) =>
        !string.IsNullOrEmpty(text) && UltraworkKeyword().IsMatch(text);

    // Блок режима максимального усилия (const Ultrawork) — в OmoPrompts.Ultrawork.cs
    // (генерируется из docs/omo/translations/ultrawork.md); дописывается к тексту хода
    // только для CLI — история и UI хранят исходное сообщение.

    // --- Цикл «до готово» (флаг work-loop) ---
    // Тексты — перевод RALPH_LOOP_TEMPLATE / ULW_LOOP_TEMPLATE и continuation-промптов
    // (docs/omo/translations/loops.md); рабочий тег <promise> — протокол детектора, не переводится.

    // Протокол хода: дописывается к каждому сообщению, пока цикл активен
    public static string WorkLoopTurn(string promise) => $"""
        [ЦИКЛ «ДО ГОТОВО»]
        Этот ход выполняется в самоссылающемся цикле разработки, который идёт до верифицированного завершения.

        Как работает цикл:
        1. Ты работаешь над задачей непрерывно.
        2. Когда считаешь, что задача ПОЛНОСТЬЮ завершена, выведи: <promise>{promise}</promise>
        3. Если ты не вывел промис, цикл автоматически подкинет следующий промпт для продолжения.
        4. После промиса система потребует финальную верификацию — не считай «{promise}» финалом, пока верификация не пройдена.

        Правила:
        - Сосредоточься на полном, а не частичном завершении задачи.
        - Не выводи промис завершения, пока задача не сделана по-настоящему.
        - Каждая итерация должна давать осмысленный прогресс к цели.
        - Застрял — пробуй другие подходы.
        - Отслеживай прогресс через todo-инструменты.
        """;

    // Автопродолжение после хода без маркера завершения
    public static string WorkLoopContinuation(string promise, int iteration, int max) => $"""
        [СИСТЕМНАЯ ДИРЕКТИВА — ЦИКЛ «ДО ГОТОВО» {iteration}/{max}]
        Продолжай. Когда закончишь, выведи <promise>{promise}</promise>.
        """;

    // Финальный верификационный ход после маркера. В оригинале верифицирует субагент-Оракул;
    // у нас верификация — тем же агентом со свидетельствами (или ревьюером-субагентом, если доступен).
    public const string WorkLoopVerification = """
        [СИСТЕМНАЯ ДИРЕКТИВА — ВЕРИФИКАЦИЯ ЦИКЛА]

        Ты вывел промис завершения. Это ещё НЕ завершает цикл.

        ТРЕБУЕТСЯ СЕЙЧАС:
        - Проверь скептически и критически, действительно ли исходная задача завершена: ищи причины, по которым она может быть всё ещё не завершена или сделана неверно.
        - Прогони фактические проверки (сборка, тесты, реальный результат) и приведи свидетельства.
        - Если доступен субагент-ревьюер — делегируй ему проверку синхронно (не в фоне) и включи исходную задачу в запрос.
        - Нашёл проблемы — почини их сейчас и перепроверь.
        - В конце подведи честный итог: что проверено, какими свидетельствами подтверждено завершение, что осталось (если осталось).
        """;

    // --- Категории делегирования (подсказка оркестратору/исполнителю задач) ---
    // const DelegationCategories — в OmoPrompts.Categories.cs (генерируется из
    // docs/omo/translations/categories.md): 8 категорий из delegate-task.
}
