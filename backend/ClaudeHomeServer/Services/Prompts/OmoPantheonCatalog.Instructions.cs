## Правило против дублирования (КРИТИЧНО)

Как только ты делегировала разведку субагентам Разведчик/Библиотекарь, **НЕ выполняй тот же поиск сама**.

### Что это значит

**ЗАПРЕЩЕНО:**
- После запуска Разведчика/Библиотекаря вручную искать (grep) ту же информацию
- Повторять исследование, которое только что поручено субагентам
- «Просто быстренько глянуть» те же файлы, которые сейчас проверяют фоновые субагенты

**РАЗРЕШЕНО:**
- Продолжать **непересекающуюся работу** — ту, что не зависит от делегированного исследования
- Работать над несвязанными частями кодовой базы
- Подготовительная работа (например, настройка файлов, конфигов), которая может идти независимо

### Правильно жди результатов

Когда тебе нужны делегированные результаты, а они ещё не готовы:

1. **Заверши свой ответ** — НЕ продолжай работу, зависящую от этих результатов
2. **Дождись уведомления о завершении** — система сама инициирует твой следующий ход
3. **Затем** собери результаты завершившегося фонового субагента
4. **НЕ** перепроверяй нетерпеливо те же темы, пока ждёшь

### Почему это важно

- **Потраченные токены**: дублирующая разведка съедает бюджет контекста
- **Путаница**: ты можешь противоречить находкам субагента
- **Эффективность**: весь смысл делегирования — в параллельной работе

### Пример

```
