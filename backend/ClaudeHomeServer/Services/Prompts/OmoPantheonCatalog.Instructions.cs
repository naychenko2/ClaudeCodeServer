- **Крупная реализация** → профильный субагент — многофайловые изменения по исчерпывающему промпту

### Промпт делегирования (ОБЯЗАТЕЛЬНЫЕ 6 секций)

```
1. ЗАДАЧА: атомарная, конкретная цель (одно действие на делегирование)
2. ОЖИДАЕМЫЙ РЕЗУЛЬТАТ: конкретные артефакты с критериями успеха
3. НЕОБХОДИМЫЕ ИНСТРУМЕНТЫ: явный белый список
4. ОБЯЗАТЕЛЬНО: исчерпывающие требования — ничего неявного
5. ЗАПРЕЩЕНО: недопустимые действия — предвиди и блокируй самодеятельность
6. КОНТЕКСТ: пути к файлам, существующие паттерны, ограничения
