### Путь «ЯСНОЕ намерение» (references/intent-clear.md)

Читай это, когда МАРШРУТИЗАЦИЯ НАМЕРЕНИЯ разрешилась в ЯСНО: пользователь знает желаемый результат, и открытыми остались только предпочтения/компромиссы, на которые репозиторий ответить не может. Сюда же входит развилка «на грани» (задай ровно один вопрос).

#### Позиция

Результатом владеет пользователь; существуют настоящие развилки, решить которые может только он. Сначала исследуй, чтобы опереться на факты, ЗАТЕМ спроси уцелевшие развилки. Ты — равный собеседник, спрашивающий только то, что действительно не можешь разрешить сам, а не следователь, собирающий список фич. Высокоточный обзор опционален только когда `review_required` = false; если пользователь уже попросил высокую точность, проведи обзор после одобрения, а не предлагай его.

#### Протокол исследования

Исследуй прежде, чем спрашивать. Разошли параллельное read-only исследование в одном ходе — внутренние паттерны/конвенции/тестовую инфраструктуру плюс внешние документы/контракты — и параллельно используй прямые Read/Grep/AST/LSP. Сортировка «факты против решений» ПЕРЕД двумя фильтрами: если ответ могут дать репозиторий/система/документация — исследуй и представь подтверждение с цитатой, никогда не вопрос; если ответить может только пользователь — вопрос может пройти в интервью; если не можешь понять, кто отвечает, — считай это решением пользователя. Остановись на достаточности (проверка готовности отвечаема), одна волна на открытый вопрос; никогда не переисследуй ради перепроверки.

#### Интервью

Сначала ФИКСАЦИЯ ТОПОЛОГИИ: из запроса и исследования перечисли 1-6 верхнеуровневых компонентов, каждый из которых может независимо преуспеть или провалиться, подтверди их ОДНИМ ходом и запиши в реестр компонентов черновика (id, результат одной строкой, статус, путь к свидетельству). НЕ схлопывай до одного компонента лишь потому, что запрос выглядит маленьким.

Затем ДВА ФИЛЬТРА (полное определение — в основном протоколе): (1) отвечаемо свидетельствами → исследуй; (2) намерение плюс обоснуемый дефолт → прими и зафиксируй, КРОМЕ решений владельца (необратимое / разрушительное / критичное для безопасности или сквозные продуктовые выборы) — они всегда выживают как вопросы.

СПРАШИВАЙ С «ПОЧЕМУ»: назови, что исследовал, почему это не разрешило вопрос и какая часть плана ветвится на ответе. 1-3 узких вопроса за ход, у каждого 2-4 варианта и твой рекомендуемый дефолт ПЕРВЫМ; пропущенный вопрос разрешается в этот дефолт. Всегда подтверждай стратегию тестирования (TDD / тесты после / без тестов — агентно-исполнимый QA включается всегда).

Прицел в САМЫЙ ТУМАННЫЙ ПРОБЕЛ (порядково, БЕЗ чисел): каждый ход целься в тот единственный открытый пробел, разрешение которого сильнее всего разблокирует план, и объясни почему одним предложением; ротируй между одинаково туманными компонентами. Каждый ход заканчивай вопросом или явным следующим шагом — никогда пассивно.

ПРОВЕРКА ГОТОВНОСТИ после каждого хода: цель определена? охват ВХОДИТ/НЕ ВХОДИТ явный? подход выбран? стратегия тестирования подтверждена? блокирующих неоднозначностей нет? Любое НЕТ — твой следующий вопрос; все ДА → представь бриф на согласование и остановись.

#### Согласование и доставка

Проведи долговечные ворота согласования (механика — в «Полном рабочем процессе»): один раз представь бриф с находками (пути), подходом и КАЖДЫМ уцелевшим решением владельца как явным вопросом с твоим рекомендуемым вариантом (пропущенный разрешается в дефолт); затем жди явного «да». Если «начать сейчас или сперва обзор?» оказался бы твоим ЕДИНСТВЕННЫМ вопросом — значит, ты задефолтил развилки, которые должен был вынести наверх; сначала перечисли их. После одобрения: создай файлы каркаса, проведи обязательную Метиду, ДОПИШИ задачи, заполни человеческий TL;DR последним. Затем либо проведи двойной высокоточный обзор, если `review_required: true`, либо представь сводку и задай ОДИН вопрос — начать работу сейчас или сперва провести двойной высокоточный обзор? Никогда не выбирай за пользователя, когда обзор не запрашивался; никогда не начинай выполнение.

#### Разобранный пример

Запрос: «добавь rate-limit 5/мин на IP для `/login`».
1. Исследование → auth-middleware в `src/auth/login.ts:40`, существующая утилита лимитера в `src/util/rate-limit.ts`, клиент Redis в `src/redis.ts`.
2. Фиксация топологии (один ход): один активный компонент — «rate-limit логина».
3. Две уцелевшие развилки, каждая спрошена С «ПОЧЕМУ»:
   - Бэкенд хранения (исследовано: репозиторий уже использует Redis; дефолт = Redis; варианты Redis / in-memory / per-node) — почему: персистентность между узлами ветвит дизайн.
   - Ответ при превышении лимита (дефолт = 429 + Retry-After; варианты 429 / 423 / молчаливый дроп) — почему: на этом ветвится контракт клиента.
4. Бриф на согласование → явное «да» → каркас → дописать задачи → если `review_required`, провести двойной обзор и доставить с квитанциями; иначе доставить с опциональным вопросом про обзор.

