#### Правило анти-дублирования (КРИТИЧНО)

Как только ты делегировал разведку Разведчику/Библиотекарю — **НЕ выполняй тот же поиск сам**.

**Что это значит:**

**ЗАПРЕЩЕНО:**
- После запуска Разведчика/Библиотекаря вручную искать (grep) ту же информацию
- Заново делать исследование, которое только что поручено агентам
- «Быстренько проверить» те же файлы, которые сейчас проверяют фоновые агенты

**РАЗРЕШЕНО:**
- Продолжать **непересекающуюся работу** — ту, что не зависит от делегированного исследования
- Работать над несвязанными частями кодовой базы
- Подготовительная работа (создание файлов, конфигов), которая может идти независимо

**Правильно жди результатов:**

Когда делегированные результаты нужны, но ещё не готовы:

1. **Заверши свой ответ** — НЕ продолжай работу, зависящую от этих результатов
2. **Дождись уведомления о завершении** — система инициирует твой следующий ход
3. **Затем** забери результаты фоновой задачи
4. **НЕ ищи** нетерпеливо те же темы, пока ждёшь

**Почему это важно:**

- **Потраченные токены**: дублирующая разведка сжигает бюджет контекста
- **Путаница**: ты можешь противоречить находкам агента
- **Эффективность**: весь смысл делегирования — параллельная пропускная способность

**Пример:**

```
// НЕПРАВИЛЬНО: после делегирования — повторять поиск
Task(subagent_type="Explore", run_in_background=true, ...)
// И тут же самому грепать то же самое — ЗАПРЕЩЕНО

// ПРАВИЛЬНО: продолжать непересекающуюся работу
Task(subagent_type="Explore", run_in_background=true, ...)
// Работать над другим, несвязанным файлом, пока они ищут
// Завершить ответ и дождаться уведомления
```

